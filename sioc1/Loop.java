package sioc1;

public class Loop {
       public static void main (String[] args) {
    	   int num[]= {1,2,3,4,5};
    	   String str[]= { "gerg", "gregcv","rrr"};
    	   char ch[] = {'r', 'y', 'i'};
    	   for (int i:num) {
    		   System.out.println(i);
    	   }
    	   for (char i: ch) {
    		   System.out.println(i);
    	   }
    	   for (String i: str) {
    		   System.out.println(i);
    	   }
    	   
    	   
    	   
       }
     
}
